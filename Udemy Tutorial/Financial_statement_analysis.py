import numpy as np

########################################################################################################################
# DATA
########################################################################################################################

revenue = np.array(
    [14574.49, 7606.46, 8611.41, 9175.41, 8058.65, 8105.44, 11496.28, 9766.09, 10305.32, 14379.96, 10713.97,
     15433.50])

expenses = np.array(
    [12051.82, 5695.07, 12319.20, 12089.72, 8658.57, 840.20, 3285.73, 5821.12, 6976.93, 16618.61, 10054.37,
     3803.96])

########################################################################################################################

########################################################################################################################
# CALCULATIONS
########################################################################################################################

Profit = np.array(revenue - expenses)

ProfitAfterTax = np.around((Profit) - (Profit * .3), decimals=0)

ProfitMargin = print(np.around((ProfitAfterTax / revenue) * 100, decimals=0))

GoodMonths = print(np.where(ProfitAfterTax > np.mean(ProfitAfterTax)))

BadMonths = print(np.where(ProfitAfterTax < np.mean(ProfitAfterTax)))

BestMonth = print(np.where(ProfitAfterTax == np.max(ProfitAfterTax)))

WorstMonth = print(np.where(ProfitAfterTax == np.min(ProfitAfterTax)))

